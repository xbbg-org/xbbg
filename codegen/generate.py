def to_pascal(key: str) -> str:
    """Convert UPPER_SNAKE to PascalCase."""
    return "".join(part.capitalize() for part in key.split("_") if part)


def sorted_items(table: dict[str, Any]) -> list[tuple[str, Any]]:
    return sorted(table.items(), key=lambda pair: pair[0])


def _render_python_enum(
    lines: list[str],
    *,
    class_name: str,
    class_doc: str,
    table: dict[str, dict[str, Any]],
    value_key: str,
) -> None:
    lines.append(f"class {class_name}(str, Enum):")
    lines.append(f'    """{class_doc}"""')
    lines.append("")
    for key, data in sorted_items(table):
        lines.append(f'    {key} = "{escape_python_string(data[value_key])}"')
        lines.append(f'    """{escape_python_doc(data["doc"])}"""')
        lines.append("")
    if lines[-1] == "":
        _ = lines.pop()
    lines.append("")
    lines.append("")


def rust_name(key: str, data: dict[str, Any]) -> str:
    value = data.get("rust_name")
    if isinstance(value, str) and value:
        return value
    return to_pascal(key)


def escape_rust_string(value: str) -> str:
    return value.replace("\\", "\\\\").replace('"', '\\"')


def escape_python_string(value: str) -> str:
    return value.replace("\\", "\\\\").replace('"', '\\"')


def escape_python_doc(value: str) -> str:
    return value.replace('"""', '\\"\\"\\"')


def load_defs(path: Path) -> dict[str, Any]:
    with path.open("rb") as handle:
        data = tomllib.load(handle)
    required = ["services", "operations", "extractors", "formats", "output_modes"]
    for key in required:
        if key not in data:
            raise ValueError(f"Missing [{key}] section in {path}")
    return data


def find_default_name(section: str, table: dict[str, dict[str, Any]], names: dict[str, str]) -> str:
    defaults = [key for key, data in table.items() if data.get("default") is True]
    if len(defaults) != 1:
        raise ValueError(f"Section [{section}] must define exactly one default item")
    return names[defaults[0]]


def validate_refs(defs: dict[str, Any]) -> None:
    service_keys = set(defs["services"].keys())
    extractor_keys = set(defs["extractors"].keys())

    for op_key, op in defs["operations"].items():
        service_key = op.get("service")
        extractor_key = op.get("extractor")
        if service_key is not None and service_key not in service_keys:
            raise ValueError(f"Operation {op_key} references unknown service {service_key}")
        if extractor_key is not None and extractor_key not in extractor_keys:
            raise ValueError(f"Operation {op_key} references unknown extractor {extractor_key}")


