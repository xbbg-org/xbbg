def is_stale(path: Path, expected: str) -> bool:
    if not path.exists():
        return True
    actual = path.read_text(encoding="utf-8")
    return actual != expected


def write_if_changed(path: Path, expected: str) -> bool:
    if not is_stale(path, expected):
        return False
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(expected, encoding="utf-8", newline="\n")
    return True


def parse_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate service enums from defs/bloomberg.toml")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Check whether generated files are up to date (CI mode)",
    )
    return parser.parse_args(argv)


def _rustfmt(text: str) -> str:
    """Run rustfmt on generated Rust code so it matches cargo fmt output."""
    rustfmt = shutil.which("rustfmt")
    if rustfmt is None:
        return text
    result = subprocess.run(
        [rustfmt, "--edition", "2021"],
        input=text,
        capture_output=True,
        text=True,
        encoding="utf-8",
        check=False,
    )
    return result.stdout if result.returncode == 0 else text


def main(argv: list[str]) -> int:
    args = parse_args(argv)

    defs = load_defs(DEFS_PATH)
    validate_refs(defs)
    rust_text = _rustfmt(render_rust(defs))
    python_text = render_python(defs)

    targets = [
        (RUST_OUT, rust_text),
        (PY_OUT, python_text),
    ]

    if args.check:
        stale: list[Path] = []
        for path, expected in targets:
            if is_stale(path, expected):
                stale.append(path)
        if stale:
            for path in stale:
                print(f"Stale generated file: {path.relative_to(ROOT)}")
            return 1
        print("Generated files are up to date.")
        return 0

    changed_any = False
    for path, expected in targets:
        changed = write_if_changed(path, expected)
        if changed:
            changed_any = True
            print(f"Updated {path.relative_to(ROOT)}")
        else:
            print(f"Unchanged {path.relative_to(ROOT)}")

    if not changed_any:
        print("No changes needed.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
